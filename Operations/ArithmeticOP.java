package Java.Operations;

public class ArithmeticOP {
    public static void main(String[] args) {
        int a = 200;
        int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
